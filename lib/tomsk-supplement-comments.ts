export function takeTomskSyntheticLines(howMany: number): string[] {
  if (howMany <= 0) return [];
  const tag = ` ${TOMSK_ROAD_SYNTHETIC_META_RU}`;
  const out: string[] = [];
  for (let i = 0; i < howMany; i += 1) {
    const base = VARIANTS[i % VARIANTS.length];
    out.push(`${base}${tag}`);
  }
  return out;
}
